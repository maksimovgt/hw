l1 = list(input("Введите элементы через пробел - ").split())

if len(l1) % 2 == 0:
    l1[::2], l1[1::2] = l1[1::2], l1[::2]
    print(f'кол-во элементов четное {l1}')
else:
    e = l1.pop()
    l1[::2], l1[1::2], = l1[1::2], l1[::2]
    l1.append(e)
    print(f'кол-во элементов нечетное {l1}')

